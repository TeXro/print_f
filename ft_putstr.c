#include "ft_printf.h"

int	ft_putstr(char *s)
{
	size_t	len;

	len = 0;
	if (s == NULL)
		return (ft_putstr("(null)"));
	while (s[len] != '\0')
	{
		write(1, &s[len], 1);
		len++;
	}
	return (len);
}
